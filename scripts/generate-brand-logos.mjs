import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, "..");
const publicDirectory = path.join(projectRoot, "public");
const outputFile = path.join(projectRoot, "lib", "brand-logos.generated.ts");

const BRAND_DIRECTORY = "brands";
const IMAGE_EXTENSIONS = new Set([".avif", ".gif", ".jpeg", ".jpg", ".png", ".svg", ".webp"]);

function formatBrandName(fileName) {
  const slug = path
    .basename(fileName, path.extname(fileName))
    .replace(/-color$/iu, "")
    .replace(/logo$/iu, "");

  if (/^honata$/iu.test(slug)) {
    return "Hondata";
  }

  if (/^holly$/iu.test(slug)) {
    return "Holley";
  }

  return slug
    .split(/[-_\s]+/u)
    .filter(Boolean)
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
    .join(" ");
}

const directoryName = fs
  .readdirSync(publicDirectory, { withFileTypes: true })
  .find((entry) => entry.isDirectory() && entry.name.toLowerCase() === BRAND_DIRECTORY)?.name;

const logos = directoryName
  ? Array.from(
      fs
        .readdirSync(path.join(publicDirectory, directoryName), { withFileTypes: true })
        .filter(
          (entry) =>
            entry.isFile() && IMAGE_EXTENSIONS.has(path.extname(entry.name).toLowerCase()),
        )
        .map((entry) => ({
          name: formatBrandName(entry.name),
          src: `/${directoryName}/${encodeURIComponent(entry.name)}`,
        }))
        .sort((a, b) => a.name.localeCompare(b.name))
        .reduce((uniqueLogos, logo) => {
          const existingLogo = uniqueLogos.get(logo.name);

          if (!existingLogo || /honatalogo/iu.test(existingLogo.src)) {
            uniqueLogos.set(logo.name, logo);
          }

          return uniqueLogos;
        }, new Map())
        .values(),
    )
  : [];

const banner = "// AUTO-GENERATED by scripts/generate-brand-logos.mjs. Do not edit by hand.\n";
const body = `export type BrandLogo = { name: string; src: string };\n\nexport const brandLogos: readonly BrandLogo[] = ${JSON.stringify(logos, null, 2)};\n`;

fs.mkdirSync(path.dirname(outputFile), { recursive: true });
fs.writeFileSync(outputFile, banner + body);

console.log(`Wrote ${logos.length} brand logos to ${path.relative(projectRoot, outputFile)}`);
